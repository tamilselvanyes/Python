# function:
def greet_with_name(name, location):
    print(f"Good morning {name} from {location}")


# positional arguments:
greet_with_name("Tamil", "Mettur")

# keyword arguments:
greet_with_name(location="Tamil", name="Mettur")

# parameter is the name like "name, location" in the previous example
# argument is the value like "Tamil, Mettur" in the previous example
